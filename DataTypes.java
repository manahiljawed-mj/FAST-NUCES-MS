class DataTypes {
    public static void main(String[] args) {
        //There are two types of datatypes in java 
        //1-Primitve 2-Non Primitve
        //Primitve Data Types
        //integer,float,char,boolean
        
        //Integer
        byte value=5;
        short value1=7;
        int value2=8;
        long value3=9;

        //range -2^7-+(2^7)-1
         System.out.println("Value of Byte is:"+value);
         System.out.println("Value of Short is:"+value1);
         System.out.println("Value of Int is:"+value2);
         System.out.println("Value of Long is:"+value3);


        //Float

        float decimalValue=5.0f;
        double decimalValue2=5.00;


         System.out.println("Value of Float is:"+decimalValue);
         System.out.println("Value of Double is:"+decimalValue2);

        //Character

        char alphabet='A';

         System.out.println("Value of Character is:"+alphabet);
        //Boolean

        boolean correct=false;

       System.out.println("Value of boolean is:"+correct);

    }
}
