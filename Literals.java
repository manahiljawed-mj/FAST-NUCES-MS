public class Literals {
    public static void main(String[] args) {
        int value=7;
        int value1=0b101;
        int value2=0x7E;

        System.out.println("Value of Int is:"+value);
        System.out.println("Value of Int is:"+value1);
        System.out.println("Value of Int is:"+value2);

        char alphabet='A';
        alphabet++;

        System.out.println("Value of Character is:"+alphabet);

        double value3=1e01;

        System.out.println("Value of Double is:"+value3);


    }
}
